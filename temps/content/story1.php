      <p>The author and Internet radio host thinks she and four of her friends left the home she shared with her husband, David Cottrell, about 15 minutes before the wildfire reached the house at 18006 Rocky Top Trail. The house burned to the ground. The 37 acres of land around it is scorched, with only a few green pine trees left standing.</p>
      <p>"I think it was a devastating series of systems screwups," she said. "Systems failed; lives were lost."</p>
      <p>The county's database of addresses within the burn area, which was provided by FirstCall, indicated that notification was received at Moeller's home.</p>
      <h5 class="subhead">This is a subhead in the story</h5>
      <p>FirstCall president Matt Teague said his company has manually corrected the addresses within the Lower North Fork fire zone.</p>
      <p>Now FirstCall is working with CenturyLink and GeoComm to make sure all 100,000 mismatched phone and address records correlate exactly to prevent future notification gaps. In some cases, an address in one database uses abbreviations for streets and in the other, the street name is spelled out, he said.</p>
      <p>In all, there are 600,000 phones and devices listed in the databases FirstCall uses for notifications throughout Jefferson and Broomfield counties.</p>
      <p>"I think we're on the right track," Teague said. "We want to make sure every address matches up."</p>
      <p>Sheriff's spokesman Mark Techmeyer said officials are trying to calculate how many people did not receive a call during the wildfire, but it has been difficult to get information from FirstCall.</p>
      <p>"This problem isn't even close to being fixed," he said.</p>
      <p>Techmeyer said he would like to know when FirstCall first knew about the mismatched addresses and if it has known about it for some time, why the problems weren't fixed.</p>
      <p>If addresses didn't match exactly, a phone was listed to a default address in the town of origin, which may not guarantee an emergency notification if the default address isn't part of the evacuation area, Kelley said.</p>
      <p>FirstCall is developing a method for residents to look up their record in the system to make sure it is accurate.</p>
      <p>Moeller said she and Cottrell lost their "magical" mountain home, their property lost significant value, they are losing income because they can't work and they are under tremendous stress.</p>
      <p>"We're not even barely scratching the surface of putting our lives back together," she said.</p>
