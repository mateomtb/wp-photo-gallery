function getCheckedValue(radioObj) {
  if(!radioObj) { return ''; }
  var radioLength = radioObj.length;
  if(radioLength === undefined) {
    return (radioObj.checked) ? radioObj.value : '';
  }
  for (var i = 0; i < radioLength; i++) {
    if(radioObj[i].checked) {
      return radioObj[i].value;
    }
  }
  return '';
}
